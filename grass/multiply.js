// sum is a function with 2 params, num1 and num2
function sum(num1, num2) {
    // this statement is adding num1 and num2 
    // irrespective of it not having any value yet
    return num1 + num2;
}

// if you remember sum is a function with two params so adding 2 and 3 in the console 
// means we are giving both num1 and num2 values 
console.log(sum(2,3));


// same logic applies here
function product(num1, num2) {
    return num1 * num2;
}
console.log(product(4,5));